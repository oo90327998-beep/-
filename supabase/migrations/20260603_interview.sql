-- ====================================================
-- RPC 函数：向量检索
-- ====================================================

-- 检索简历片段
CREATE OR REPLACE FUNCTION search_resume_chunks(
    query_session_id UUID,
    query_embedding TEXT,
    top_k INT DEFAULT 3
) RETURNS TABLE(content TEXT, chunk_index INT, similarity FLOAT) AS $$
BEGIN
    RETURN QUERY
    SELECT
        rc.content,
        rc.chunk_index,
        1 - (rc.embedding <=> query_embedding::VECTOR) AS similarity
    FROM resume_chunks rc
    WHERE rc.session_id = query_session_id
    ORDER BY rc.embedding <=> query_embedding::VECTOR
    LIMIT top_k;
END;
$$ LANGUAGE plpgsql;

-- 检索面试题库
CREATE OR REPLACE FUNCTION search_question_bank(
    query_embedding TEXT,
    top_k INT DEFAULT 5
) RETURNS TABLE(question TEXT, category VARCHAR, role VARCHAR, difficulty VARCHAR, tags TEXT[], similarity FLOAT) AS $$
BEGIN
    RETURN QUERY
    SELECT
        qb.question,
        qb.category,
        qb.role,
        qb.difficulty,
        qb.tags,
        1 - (qb.embedding <=> query_embedding::VECTOR) AS similarity
    FROM question_bank qb
    ORDER BY qb.embedding <=> query_embedding::VECTOR
    LIMIT top_k;
END;
$$ LANGUAGE plpgsql;
